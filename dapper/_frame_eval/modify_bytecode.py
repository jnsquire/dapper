# public helper used by instrumented bytecode


def _dapper_breakpoint(line: int) -> None:
    """Called from rewritten bytecode when a breakpoint check occurs.

    The implementation mirrors the behaviour once generated by
    ``create_breakpoint_wrapper_code`` and avoids importing heavy
    debugger modules on the fast path.  Keeping it in this module makes the
    global name stable for instrumentation.
    """
    try:
        sys_module = cast("Any", sys)
        frame = sys._getframe(1)
        filename = frame.f_code.co_filename
        lineno = line

        if hasattr(sys_module, "_pydevd_frame_eval"):
            debugger_info = sys_module._pydevd_frame_eval
            if "check_breakpoint" in debugger_info:
                should_stop = debugger_info["check_breakpoint"](filename, lineno, frame)
                if should_stop:
                    debugger_info["stop_at_frame"](frame)
                    return

        # Fallback: call trace function if set
        if hasattr(sys_module, "trace_function") and sys_module.trace_function:
            sys_module.trace_function(frame, "line", None)
    except Exception:
        pass


class BytecodeInfo(TypedDict):
    """Type definition for bytecode information dictionary.

    Attributes:
        instruction_count: Number of instructions in the bytecode
        has_breakpoints: Whether the bytecode contains breakpoint sequences
        stack_size: Maximum stack size required by the code
        flags: Code object flags (co_flags)
        first_lineno: First line number in the source code
        filename: Name of the source file
        name: Name of the code object
        constants_count: Number of constants in the code object
        names_count: Number of names in the code object
        varnames_count: Number of local variable names in the code object
    """

    instruction_count: int
    has_breakpoints: bool
    stack_size: int
    flags: int
    first_lineno: int
    filename: str
    name: str
    constants_count: int
    names_count: int
    varnames_count: int


class BytecodeErrorInfo(TypedDict):
    """Type definition for bytecode error information.

    Used when bytecode analysis fails.

    Attributes:
        error: Error message
        filename: Name of the source file if available, otherwise 'unknown'
        name: Name of the code object if available, otherwise 'unknown'

    """

    error: str
    filename: str
    name: str


# current metadata layout version.  Bump when the instrumentation pattern
# changes so existing cache entries are ignored.
_BYTECODE_META_VERSION = 1


class BytecodeModifier:
    """Advanced bytecode modification system for frame evaluation.

    The modifier keeps an in-memory cache keyed by the *original* code
    object (identity) along with a small fingerprint derived from the
    requested breakpoint set.  Using the object itself as the key makes
    invalidation on file reloads or garbage collection much simpler.  The
    fingerprint allows multiple breakpoint configurations to coexist for the
    same code object.
    """

    def __init__(self):
        self.breakpoint_counter = 0
        # primary cache: original_code -> {fingerprint: modified_code}
        # type: ignore[valid-type]  # quoting removed via annotations
        self.modified_code_objects: dict[CodeType, dict[int, CodeType]] = {}
        self.optimization_enabled = True

    def _instrument_nested(
        self,
        code_obj: CodeType,
        breakpoint_lines: set[int],
        debug_mode: bool,
    ) -> CodeType:
        """Recursive helper that instruments nested code objects."""
        if not breakpoint_lines:
            return code_obj

        consts = list(code_obj.co_consts)
        changed_consts = False
        for idx, const in enumerate(consts):
            if isinstance(const, types.CodeType):
                _, new_const = self.inject_breakpoints(const, breakpoint_lines, debug_mode)
                if new_const is not const:
                    consts[idx] = new_const
                    changed_consts = True
        if changed_consts:
            try:
                code_obj = code_obj.replace(co_consts=tuple(consts))
            except Exception:
                _, code_obj = _code_object_builder.rebuild_code_object(
                    code_obj,
                    get_instructions(code_obj),
                )
                # ignore failure
        return code_obj

    def _apply_injection(
        self,
        code_obj: CodeType,
        breakpoint_lines: set[int],
        debug_mode: bool,
    ) -> tuple[bool, CodeType]:
        """Perform cache lookup, injection, and storage."""
        fp = None
        try:
            frame_evaluator = import_module("dapper._frame_eval._frame_evaluator")
            fp = hash(tuple(sorted(breakpoint_lines)))
            existing = self.modified_code_objects.get(code_obj)
            if existing and fp in existing:
                modified_code = existing[fp]
                frame_evaluator._store_modified_code_for_evaluation(
                    code_obj,
                    modified_code,
                    breakpoint_lines,
                )
                return True, modified_code

            instructions = get_instructions(code_obj)
            injection_points = self._find_injection_points(instructions, breakpoint_lines)
            if not injection_points:
                return True, code_obj

            new_instructions = self._create_breakpoint_instructions(instructions, injection_points)
            accepted, modified_code = self._rebuild_code_object(code_obj, new_instructions)
            if not accepted:
                if existing and fp in existing:
                    del existing[fp]
                telemetry.record_bytecode_rollback(
                    filename=getattr(code_obj, "co_filename", "unknown"),
                    name=getattr(code_obj, "co_name", "unknown"),
                )
                return False, code_obj

            self.modified_code_objects.setdefault(code_obj, {})[fp] = modified_code
            frame_evaluator._store_modified_code_for_evaluation(
                code_obj,
                modified_code,
                breakpoint_lines,
            )
        except Exception as e:
            if debug_mode:
                print(f"Error injecting breakpoints: {e}")
            telemetry.record_bytecode_injection_failed(
                filename=getattr(code_obj, "co_filename", "unknown"),
                name=getattr(code_obj, "co_name", "unknown"),
            )
            existing = self.modified_code_objects.get(code_obj)
            if fp is not None and existing and fp in existing:
                del existing[fp]
            return False, code_obj
        else:
            return True, modified_code

    def inject_breakpoints(
        self,
        code_obj: CodeType,
        breakpoint_lines: set[int],
        debug_mode: bool = False,
    ) -> tuple[bool, CodeType]:
        """Inject breakpoints into a code object at specified lines.

        Args:
            code_obj: The original code object to modify
            breakpoint_lines: Set of line numbers with breakpoints
            debug_mode: Enable debug mode for verbose logging

        Returns:
            tuple: (success, modified_code_obj)
        """
        code_obj = self._instrument_nested(code_obj, breakpoint_lines, debug_mode)
        if not breakpoint_lines:
            return True, code_obj
        return self._apply_injection(code_obj, breakpoint_lines, debug_mode)

    def create_breakpoint_wrapper_code(self, line: int) -> CodeType:
        """Create a code object that serves as a breakpoint wrapper.

        Args:
            line: The line number for the breakpoint

        Returns:
            CodeType: Compiled wrapper code object

        """
        wrapper_source = f'''
def __dapper_breakpoint_wrapper_{line}():
    """Breakpoint wrapper for line {line}."""
    try:
        # Get the current frame
        import sys
        frame = sys._getframe(1)

        # Check if we should stop at this line
        filename = frame.f_code.co_filename
        lineno = {line}

        # Call the debugger's breakpoint check
        if hasattr(sys, '_pydevd_frame_eval'):
            debugger_info = sys._pydevd_frame_eval
            if 'check_breakpoint' in debugger_info:
                should_stop = debugger_info['check_breakpoint'](filename, lineno, frame)
                if should_stop:
                    debugger_info['stop_at_frame'](frame)
                    return

        # Fallback: check for trace function
        if hasattr(sys, 'trace_function') and sys.trace_function:
            sys.trace_function(frame, 'line', None)

    except Exception:
        # Silently ignore errors to avoid breaking execution
        pass

__dapper_breakpoint_wrapper_{line}()
'''

        # Compile the wrapper
        return compile(wrapper_source, f"<dapper_breakpoint_wrapper_{line}>", "exec")

    def optimize_code_object(self, code_obj: CodeType) -> CodeType:
        """Optimize a code object for better frame evaluation performance.

        Args:
            code_obj: The code object to optimize

        Returns:
            CodeType: Optimized code object

        """
        if not self.optimization_enabled:
            return code_obj

        try:
            instructions = get_instructions(code_obj)
            optimized_instructions = self._optimize_instructions(instructions)
            _accepted, optimized = self._rebuild_code_object(code_obj, optimized_instructions)
        except Exception:
            telemetry.record_bytecode_optimization_failed(
                filename=getattr(code_obj, "co_filename", "unknown"),
                name=getattr(code_obj, "co_name", "unknown"),
            )
            return code_obj
        else:
            return optimized

    def remove_breakpoints(self, code_obj: CodeType) -> CodeType:
        """Remove any injected breakpoints from a code object.

        Args:
            code_obj: The code object to clean

        Returns:
            CodeType: Cleaned code object

        """
        try:
            instructions = get_instructions(code_obj)
            cleaned_instructions = []

            i = 0
            while i < len(instructions):
                instr = instructions[i]

                # Skip breakpoint instruction sequences
                if self._is_breakpoint_sequence(instructions, i):
                    # Skip the entire breakpoint sequence
                    i += self._get_breakpoint_sequence_length(instructions, i)
                    continue

                cleaned_instructions.append(instr)
                i += 1

            _accepted, cleaned = self._rebuild_code_object(code_obj, cleaned_instructions)

        except Exception:
            telemetry.record_bytecode_optimization_failed(
                filename=getattr(code_obj, "co_filename", "unknown"),
                name=getattr(code_obj, "co_name", "unknown"),
            )
            return code_obj
        else:
            return cleaned

    def _get_cache_key(
        self,
        code_obj: CodeType,
        breakpoint_lines: set[int],
    ) -> tuple[int, int, int]:
        """Generate a cache key used for diagnostics and metadata storage.

        The real cache is stored in :attr:`modified_code_objects` by identity,
        but we still expose a small tuple so tests and telemetry can verify the
        fingerprint without digging into object tables.

        Returns:
            (id(code_obj), fingerprint, meta_version)
        """
        fp = hash(tuple(sorted(breakpoint_lines)))
        return (id(code_obj), fp, _BYTECODE_META_VERSION)

    def _find_injection_points(
        self,
        instructions: list[dis.Instruction],
        breakpoint_lines: set[int],
    ) -> dict[int, int]:
        """Find the best injection points for breakpoints.

        Returns a dict mapping line numbers to instruction indices.
        """
        injection_points = {}
        current_line: int | None = None

        for i, instr in enumerate(instructions):
            # Python 3.13+ exposes the actual source line in ``line_number`` and
            # uses ``starts_line`` as a boolean marker. Older versions store the
            # line number directly in ``starts_line``. Track the active line so
            # callers can pass values discovered via ``co_lines()`` across both
            # metadata layouts.
            line_number = getattr(instr, "line_number", None)
            if isinstance(line_number, int):
                current_line = line_number
            elif isinstance(instr.starts_line, int):
                current_line = instr.starts_line

            if current_line is not None and current_line in breakpoint_lines:
                # Prefer to inject after certain instruction types
                if instr.opname in ("LOAD_CONST", "LOAD_FAST", "LOAD_GLOBAL", "STORE_FAST"):
                    injection_points.setdefault(current_line, i + 1)
                else:
                    injection_points.setdefault(current_line, i)

        return injection_points

    def _create_breakpoint_instructions(
        self,
        original_instructions: list[dis.Instruction],
        injection_points: dict[int, int],
    ) -> list[dis.Instruction]:
        """Create new instruction list with breakpoints injected."""
        new_instructions = []
        breakpoint_code_cache = {}

        for i, instr in enumerate(original_instructions):
            new_instructions.append(instr)

            # Check if we need to inject a breakpoint after this instruction
            line_to_check = None
            for line, injection_index in injection_points.items():
                if injection_index == i + 1:  # Inject after current instruction
                    line_to_check = line
                    break

            if line_to_check is not None:
                # Get or create breakpoint wrapper code
                if line_to_check not in breakpoint_code_cache:
                    breakpoint_code_cache[line_to_check] = self.create_breakpoint_wrapper_code(
                        line_to_check,
                    )

                # Add breakpoint check instructions
                breakpoint_instrs = self._create_breakpoint_check_instructions(line_to_check)
                new_instructions.extend(breakpoint_instrs)

        return new_instructions

    def _create_breakpoint_check_instructions(self, line: int) -> list[dis.Instruction]:
        """Create a canonical instrumentation sequence for a breakpoint.

        The sequence is deliberately minimal and version-stable so the safety
        validator can recognise and preserve it.  It simply loads the line
        number constant, looks up a module-level helper, and calls it.
        """
        instructions = [
            make_instruction(
                opname="LOAD_GLOBAL",
                opcode=LOAD_GLOBAL,
                arg=-1,
                argval="_dapper_breakpoint",
                argrepr="_dapper_breakpoint",
                offset=0,
                starts_line=None,
                is_jump_target=False,
            ),
        ]
        instructions.extend(
            self._make_inline_cache_instructions(LOAD_GLOBAL, len(instructions) * 2)
        )
        instructions.append(
            make_instruction(
                opname="LOAD_CONST",
                opcode=LOAD_CONST,
                arg=-1,
                argval=line,
                argrepr=str(line),
                offset=len(instructions) * 2,
                starts_line=None,
                is_jump_target=False,
            ),
        )
        call_opname = "CALL" if sys.version_info >= (3, 11) else "CALL_FUNCTION"
        instructions.append(
            make_instruction(
                opname=call_opname,
                opcode=CALL_FUNCTION,
                arg=1,
                argval=1,
                argrepr="",
                offset=len(instructions) * 2,
                starts_line=None,
                is_jump_target=False,
            ),
        )
        instructions.extend(
            self._make_inline_cache_instructions(CALL_FUNCTION, len(instructions) * 2),
        )
        instructions.append(
            make_instruction(
                opname="POP_TOP",
                opcode=POP_TOP,
                arg=None,
                argval=None,
                argrepr="",
                offset=len(instructions) * 2,
                starts_line=None,
                is_jump_target=False,
            ),
        )
        return instructions

    def _make_inline_cache_instructions(
        self,
        opcode: int,
        offset: int,
    ) -> list[dis.Instruction]:
        """Return the CACHE pseudo-instructions required after *opcode*."""
        if sys.version_info < (3, 11):
            return []

        cache_entries = getattr(dis, "_inline_cache_entries", ())
        count = 0
        try:
            count = cache_entries[opcode]
        except (IndexError, KeyError, TypeError):
            if isinstance(cache_entries, dict):
                count = cache_entries.get(opcode, 0)

        return [
            make_instruction(
                opname="CACHE",
                opcode=CACHE,
                arg=0,
                argval=None,
                argrepr="",
                offset=offset + (index * 2),
                starts_line=None,
                is_jump_target=False,
            )
            for index in range(count)
        ]

    def _optimize_instructions(self, instructions: list[dis.Instruction]) -> list[dis.Instruction]:
        """Optimize a list of instructions for better performance."""
        optimized = []
        i = 0

        while i < len(instructions):
            instr = instructions[i]

            # Skip certain debug instructions in optimized mode
            if instr.opname == "POP_TOP" and i > 0:
                prev_instr = instructions[i - 1]
                if prev_instr.opname == "LOAD_CONST" and prev_instr.argval is None:
                    # Skip LOAD_CONST None; POP_TOP sequence
                    i += 1
                    continue

            # Optimize consecutive LOAD_CONST operations
            if instr.opname == "LOAD_CONST" and i + 1 < len(instructions):
                next_instr = instructions[i + 1]
                if next_instr.opname == "POP_TOP" and next_instr.arg is None:
                    # Skip this optimization for now to maintain correctness
                    pass

            optimized.append(instr)
            i += 1

        return optimized

    def _is_breakpoint_sequence(
        self,
        instructions: list[dis.Instruction],
        start_index: int,
    ) -> bool:
        """Check if instructions starting at start_index form a breakpoint sequence."""
        return self._match_breakpoint_sequence(instructions, start_index) > 0

    def _match_breakpoint_sequence(
        self,
        instructions: list[dis.Instruction],
        start_index: int,
    ) -> int:
        """Return the full breakpoint-sequence length or ``0`` if unmatched."""

        def skip_caches(index: int) -> int:
            while index < len(instructions) and instructions[index].opname == "CACHE":
                index += 1
            return index

        def read_expected(index: int, *opnames: str) -> tuple[dis.Instruction | None, int]:
            index = skip_caches(index)
            if index >= len(instructions):
                return None, index

            instr = instructions[index]
            if instr.opname not in opnames:
                return None, index
            return instr, index + 1

        if start_index >= len(instructions):
            return 0

        first = instructions[start_index]
        if first.opname == "LOAD_GLOBAL":
            load_global = first
            load_const, next_index = read_expected(start_index + 1, "LOAD_CONST")
        elif first.opname == "LOAD_CONST":
            load_const = first
            load_global, next_index = read_expected(start_index + 1, "LOAD_GLOBAL")
        else:
            return 0

        if load_global is None or load_const is None:
            return 0

        call_instr, next_index = read_expected(next_index, "CALL", "CALL_FUNCTION")
        pop_top_instr, end_index = read_expected(next_index, "POP_TOP")

        if not (
            load_global.argval == "_dapper_breakpoint"
            and isinstance(load_const.argval, int)
            and call_instr is not None
            and pop_top_instr is not None
        ):
            return 0

        return end_index - start_index

    def _get_breakpoint_sequence_length(
        self,
        instructions: list[dis.Instruction],
        start_index: int,
    ) -> int:
        """Get the length of a breakpoint instruction sequence."""
        length = self._match_breakpoint_sequence(instructions, start_index)
        return length or 1

    def _rebuild_code_object(
        self,
        original_code: CodeType,
        new_instructions: list[dis.Instruction],
    ) -> tuple[bool, CodeType]:
        """Delegate to the module-level :func:`rebuild_code_object`."""
        return _code_object_builder.rebuild_code_object(original_code, new_instructions)


